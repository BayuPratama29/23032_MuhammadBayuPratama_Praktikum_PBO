public class HurufZkeA {
    public static void main(String[] args) {
        // Menampilkan huruf dari Z sampai A
        System.out.println("Huruf dari Z sampai A:");
        for (char ch = 'Z'; ch >= 'A'; ch--) {
            System.out.print(ch + " "); // Cetak huruf dari Z ke A
        }
    }
}

